export const getRandomQuestion = (
  mode: InterviewMode,
  phase: 'iceBreak' | 'phase2' | 'phase3' | 'closing' | 'spice',
  categoryIndex?: number
): string | null => {
  const config = getInterviewMode(mode);
  if (!config) return null;

  const bank = config.questionBank;

  if (phase === 'iceBreak') {
    return bank.iceBreak[Math.floor(Math.random() * bank.iceBreak.length)];
  }

  if (phase === 'closing') {
    return bank.closing[Math.floor(Math.random() * bank.closing.length)];
  }

  if (phase === 'spice' && bank.spice) {
    return bank.spice[Math.floor(Math.random() * bank.spice.length)];
  }

  if (phase === 'phase2' || phase === 'phase3') {
    const categories = phase === 'phase2' ? bank.phase2 : bank.phase3;
    const catIdx = categoryIndex ?? Math.floor(Math.random() * categories.length);
    const category = categories[catIdx];
    if (category) {
      return category.questions[Math.floor(Math.random() * category.questions.length)];
    }
  }

  return null;
};
